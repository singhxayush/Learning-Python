# String Methods

print(len("12345"), "\n")

quote = "Never Give Up"
print(len(quote))
print(quote.upper())
print(quote.lower())
print(quote.capitalize(), "\n")

print(quote.count('G'), "\n")


# kind of a replacement printing but not actual replacement
print(quote.replace('Up', 'In'))

# NOTE : strings in python is immutable, it can only be replaced as a new, but cant be changed

